package package1;

public class CalculatorInheritance {
	private void add() {
		System.out.println("Add Operation");
	}

	void sub() {
		System.out.println("Subtraction Operation");
	}

	protected void mul() {
		System.out.println("Multiplication Operation");
	}
	
	public void div() {
		System.out.println("Division Operation");
	}
}
